/**
 * Get all Miralbo static properties
 */
export function getMiralboStaticProperties(): MiralboProperty[] {
  return MIRALBO_STATIC_PROPERTIES;
}

/**
 * Get a single Miralbo property by reference
 */
export function getMiralboPropertyByRef(ref: string): MiralboProperty | undefined {
  return MIRALBO_STATIC_PROPERTIES.find(p =>
    p.ref.toLowerCase() === ref.toLowerCase() ||
    p.slug.toLowerCase() === ref.toLowerCase()
  );
}

/**
 * Get Miralbo properties by town
 */
export function getMiralboPropertiesByTown(town: string): MiralboProperty[] {
  return MIRALBO_STATIC_PROPERTIES.filter(p =>
    p.town.toLowerCase().includes(town.toLowerCase())
  );
}
